//Дано число. Если оно больше 10, то вывести его на консоль, если же оно меньше 3, 
//то увеличить его на 11 и вывести на консоль, в остальных случаях уменьшить число на 100.

let a = 2;
if (a > 10) {
  console.log(a)
} else if (a < 3) {
  console.log(a + 11)
} else {
  console.log(a - 100)
}